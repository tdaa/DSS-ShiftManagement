    private void mostrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mostrarButtonActionPerformed
        // botao mostrar turnos
        String uc = this.ucsList.getSelectedValue();
        this.selecionada = uc;
        if(uc != null){
            uc = uc.substring(0, uc.indexOf(" "));
            atualizaListaTurnosDisponiveis(uc);
            this.label.setText(uc);
        }
    }//GEN-LAST:event_mostrarButtonActionPerformed

    private void escolherButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_escolherButtonActionPerformed
        // botao escolher turno
        String t = this.turnosUCList.getSelectedValue();
        if(t != null && this.selecionada!=null){
            t = t.substring(0, t.indexOf(" "));
            this.system.addTurnoParaAluno(this.aluno, t, this.selecionada);
            atualizaJanela();
        }
    }//GEN-LAST:event_escolherButtonActionPerformed

