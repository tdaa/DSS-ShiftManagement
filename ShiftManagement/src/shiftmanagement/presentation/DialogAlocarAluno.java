    private void atualizaJanela(){
        atualizaListaUcs();
        atualizaListaTurnos();
    }
    
    private void atualizaListaUcs(){
        DefaultListModel<String> dlm = new DefaultListModel<>();
        for(String s: this.system.getUCsAluno(aluno)){
            dlm.addElement(s);
        }
        this.ucsList.setModel(dlm);
    }
    
    private void atualizaListaTurnos(){
        DefaultListModel<String> dlm = new DefaultListModel<>();
        for(String s: this.system.getAlunoHorario(aluno)){
            dlm.addElement(s);
        }
        this.turnosList.setModel(dlm);
    }
    
    private void atualizaListaTurnosDisponiveis(String codigoUC){
        DefaultListModel<String> dlm = new DefaultListModel<>();
        for(String s: this.system.getTurnosUc(codigoUC)){
            dlm.addElement(s);
        }
        this.turnosUCList.setModel(dlm);
    }

